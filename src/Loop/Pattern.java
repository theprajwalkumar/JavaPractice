package Loop;

public class Pattern {

	public static void main(String[] args) {
		for(int x=0;x<=5;x++) {
			for(int y=0;y<=x;y++) {
				
				System.out.print("*");
				
				
				
				
			}
			System.out.println();
		}

	
	for(int z=0;z<=5;z++) {
		for (int k=5;k>=z;k--) {
			System.out.print("*");
			
		}
		System.out.println();
	}
		
	}
}


