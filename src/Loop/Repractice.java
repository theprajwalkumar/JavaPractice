package Loop;

public class Repractice {

	public static void main(String[] args) {
		for(int x=0;x<=5;x++) {
			for(int y=0;y<=x;y++) {
				System.out.print(y);
			}
			System.out.println();
		}
		for(int z=0;z<=5;z++) {
			for (int k=5;z<=k;k--) {
				System.out.print(k);
			}
			System.out.println();
		}

	}

}
